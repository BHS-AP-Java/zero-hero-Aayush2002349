  private void displayBun(int size){
    String crust = "HH";
    int width = size * 7/8;
    int height = size / 4;
    String layer = "";

    //A space is half of the total
    for(int i = 0; i < size - (size*7/8); i++){
      layer += " ";
    }
    for(int i = 0; i < width; i++){
      layer += crust;
    }
    for(int j = 0; j < height; j++){
      System.out.println(layer);
    }
  }

  private void displayFilling(int size) {
    String crust = "##";
    int width = size;

    int height = size / 5;
    String layer = "";
    for (int i = 0; i < width; i++) {
      layer += crust;
    }
    for (int j = 0; j < height; j++) {
      System.out.println(layer);
    }
  }
